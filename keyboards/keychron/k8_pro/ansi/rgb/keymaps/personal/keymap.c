/*
 * Define the layers to be QWERTY_BASE, (COLEMAK_BASE), FN_LAYER, OPTION_LAYER, DEBUG_LAYER
 *
 * QWERTY_BASE contains the normal keyboard keys in Qwerty layout.
 * COLEMAK_BASE contains the normal keyboard keys in Colemak-DH layout.
 * FN_LAYER contains F1-F2 functions, RGB colors, Battery level, NKRO, Bluetooth hosts and other keys
 * OPTION_LAYER contains Numpad, mouse emulation, other custom keys, macros and layer-toggling keys
 * DEBUG_LAYER contains tapping term configs and quantum keycodes
 * GAMING_LAYER contains base Qwerty keys without no delay, with special keys like Super and Fn having multi-functions.
 *
 * Switching between base layers can be implemented using DF(layer).
 * FN_LAYER and OPTION_LAYER are accessed using MO(layer).
 *
 * OPTION_LAYER implement TG(layer) on F-keys for switching to other layers.
 *
 * DEBUG_LAYER, GAMING_LAYER and others are special layers, and should be disabled using TG(current layer).
 *
 * Software features:
 *     Caps Word: Enabled through CW_TOGG.
 *     Combos: Enabled, and not necessarily pressed in order. (No use yet).
 *     Tap Dance: Enabled. (No use yet).
 *     Tap-Hold: Default mode. Can use "Timeless homerow mods" configs too.
 *     Space Cadet Shift: Mod-Tap Parentheses and Shift.
 *
 * Other settings:
 * Replace CapsLock with Mod-Tap ESC and LCTL
 * Tri-layer, for an additional layer when both Fn and Option are held.
 * WPM calculation, for setting colors when high WPM are reached.
 * Macros.
 * Repeat Key (No place yet).
*/
